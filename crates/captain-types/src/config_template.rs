    #[test]
    fn reconcile_preserves_user_edits_and_comments() {
        let user = r#"
# My personal config, keep my comments
stt_model = "whisper-small"

[default_model]
# I use OpenRouter
provider = "openrouter"
model = "anthropic/claude-sonnet-4.6"
api_key_env = "OPENROUTER_API_KEY"
"#;
        let r = reconcile_string(user, "v3.14").unwrap();
        assert!(!r.added_keys.is_empty());
        // User's custom comment survives.
        assert!(r
            .reconciled
            .contains("# My personal config, keep my comments"));
        assert!(r.reconciled.contains("# I use OpenRouter"));
        // Overridden value is preserved.
        assert!(r.reconciled.contains("provider = \"openrouter\""));
        assert!(!r.reconciled.contains("provider = \"codex\""));
        // Missing section appears.
        assert!(r.reconciled.contains("[learning]"));
    }

    #[test]
    fn reconcile_idempotent_on_second_run() {
        let user = r#"
[default_model]
provider = "openrouter"
model = "x"
api_key_env = "OPENROUTER_API_KEY"
"#;
        let first = reconcile_string(user, "v3.14").unwrap();
        assert!(!first.added_keys.is_empty());
        let second = reconcile_string(&first.reconciled, "v3.14").unwrap();
        assert!(second.added_keys.is_empty(), "second run must be a no-op");
    }

