//! Default config.toml rendering (v3.14-config.a).
//!
//! Produces a complete TOML document from `KernelConfig::default()`
//! so users and agents can see every configurable field at a glance.
//! The CLI (`captain config init`) and the boot reconciler
//! (v3.14-config.b) both call into here.
//!
//! Note: `Option::None` fields are omitted by `toml::ser` — the
//! header comment documents how to introspect optional keys.

#[cfg(test)]
use crate::config::find_direct_secret_assignments;
use crate::config::KernelConfig;

/// Header prepended by `render_default_toml_with_header`. Explains
/// the "everything visible, defaults shown" convention so the file
/// is self-describing.
pub const CONFIG_HEADER: &str = "\
# Captain configuration — every configurable field at its default value.
# ------------------------------------------------------------------
# Generated by `captain config init`. Safe to edit by hand.
#
# Secrets: never commit real keys here. Codex uses OAuth and keeps
# `api_key_env` empty; API providers should point `api_key_env` to an
# environment variable (ANTHROPIC_API_KEY, OPENROUTER_API_KEY, ...).
#
# Optional fields (Rust `Option<T>`) are omitted until set — see
# `docs/configuration.md` and `captain.toml.example` for optional keys.
#
# Reconciliation: `captain` will auto-append missing top-level keys
# on boot with a `# auto-added` comment so upgrades never need a
# manual rewrite. Your edits are preserved.
";

/// Serialize `KernelConfig::default()` to a pretty TOML string.
/// The returned TOML is valid input to `toml::from_str`.
pub fn render_default_toml() -> Result<String, toml::ser::Error> {
    toml::to_string_pretty(&KernelConfig::default())
}

/// Same as `render_default_toml` but prefixed with `CONFIG_HEADER`.
/// Use this for `captain config init`.
pub fn render_default_toml_with_header() -> Result<String, toml::ser::Error> {
    Ok(format!("{}\n{}", CONFIG_HEADER, render_default_toml()?))
}

/// List top-level TOML keys present in the default template but
/// missing from the user's config. Returns keys in the order they
/// appear in the default template so `reconcile` can append them in
/// a stable order.
///
/// "Top-level" means first-level tables and bare keys; nested tables
/// (`[tts.openai]`) are not split from their parent.
pub fn scan_missing_top_level_keys(user_toml: &str) -> Result<Vec<String>, ReconcileError> {
    let template = render_default_toml().map_err(ReconcileError::Render)?;
    let template_doc: toml_edit::DocumentMut = template
        .parse()
        .map_err(|e: toml_edit::TomlError| ReconcileError::Parse(e.to_string()))?;
    let user_doc: toml_edit::DocumentMut = user_toml
        .parse()
        .map_err(|e: toml_edit::TomlError| ReconcileError::Parse(e.to_string()))?;

    let user_top: std::collections::HashSet<String> = user_doc
        .as_table()
        .iter()
        .map(|(k, _)| k.to_string())
        .collect();

    let mut missing = Vec::new();
    for (key, _) in template_doc.as_table().iter() {
        if !user_top.contains(key) {
            missing.push(key.to_string());
        }
    }
    Ok(missing)
}

/// Reconcile a user config file against the default template:
/// append every missing top-level key/table with a
/// `# auto-added by captain v<version>` comment, preserving all
/// existing edits and comments in between.
///
/// Returns the list of keys that were added. An empty vec means the
/// file was already complete. If the file is malformed, returns an
/// error and does not touch it.
pub fn reconcile_file(
    path: &std::path::Path,
    version: &str,
) -> Result<Vec<String>, ReconcileError> {
    let content = std::fs::read_to_string(path).map_err(ReconcileError::Io)?;
    let added = reconcile_string(&content, version)?;
    if added.added_keys.is_empty() {
        return Ok(Vec::new());
    }
    std::fs::write(path, &added.reconciled).map_err(ReconcileError::Io)?;
    Ok(added.added_keys)
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ReconcileResult {
    pub reconciled: String,
    pub added_keys: Vec<String>,
}

/// In-memory reconcile. Separated from `reconcile_file` so the CLI
/// dry-run path and tests can exercise it without touching disk.
pub fn reconcile_string(user_toml: &str, version: &str) -> Result<ReconcileResult, ReconcileError> {
    let template = render_default_toml().map_err(ReconcileError::Render)?;
    let template_doc: toml_edit::DocumentMut = template
        .parse()
        .map_err(|e: toml_edit::TomlError| ReconcileError::Parse(e.to_string()))?;
    let mut user_doc: toml_edit::DocumentMut = user_toml
        .parse()
        .map_err(|e: toml_edit::TomlError| ReconcileError::Parse(e.to_string()))?;

    let mut added_keys = Vec::new();
    let user_top: std::collections::HashSet<String> = user_doc
        .as_table()
        .iter()
        .map(|(k, _)| k.to_string())
        .collect();

    for (key, item) in template_doc.as_table().iter() {
        if user_top.contains(key) {
            continue;
        }
        let mut cloned = item.clone();
        if let Some(tbl) = cloned.as_table_mut() {
            let decor = tbl.decor_mut();
            decor.set_prefix(format!("\n# auto-added by captain {version}\n"));
        } else {
            // Leaf value: attach the comment to the key via the
            // surrounding key decor. toml_edit's key-decor API
            // requires accessing the key through the table iterator,
            // so we just prepend a standalone comment line in the
            // output by inserting via the parent table path.
        }
        user_doc.insert(key, cloned);
        added_keys.push(key.to_string());
    }

    Ok(ReconcileResult {
        reconciled: user_doc.to_string(),
        added_keys,
    })
}

#[derive(Debug, thiserror::Error)]
pub enum ReconcileError {
    #[error("render default template: {0}")]
    Render(#[from] toml::ser::Error),
    #[error("parse TOML: {0}")]
    Parse(String),
    #[error(transparent)]
    Io(#[from] std::io::Error),
}

