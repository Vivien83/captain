    #[test]
    fn reconcile_file_roundtrips_through_disk() {
        use tempfile::TempDir;
        let dir = TempDir::new().unwrap();
        let path = dir.path().join("config.toml");
        std::fs::write(
            &path,
            r#"
[default_model]
provider = "openrouter"
model = "x"
api_key_env = "OPENROUTER_API_KEY"
"#,
        )
        .unwrap();
        let added = reconcile_file(&path, "v3.14").unwrap();
        assert!(added.contains(&"learning".to_string()));
        let body = std::fs::read_to_string(&path).unwrap();
        assert!(body.contains("[learning]"));
        // Second run is a no-op: no disk write either, but current
        // impl always writes on any added_keys; after idempotence
        // there are no added_keys so body stays byte-identical.
        let body_before = body.clone();
        let added_again = reconcile_file(&path, "v3.14").unwrap();
        assert!(added_again.is_empty());
        let body_after = std::fs::read_to_string(&path).unwrap();
        assert_eq!(body_before, body_after);
    }

    #[test]
    fn reconcile_errors_out_on_malformed_file() {
        use tempfile::TempDir;
        let dir = TempDir::new().unwrap();
        let path = dir.path().join("broken.toml");
        std::fs::write(&path, "not = valid [toml").unwrap();
        let err = reconcile_file(&path, "v3.14").unwrap_err();
        assert!(matches!(err, ReconcileError::Parse(_)));
        // File left alone.
        let body = std::fs::read_to_string(&path).unwrap();
        assert_eq!(body, "not = valid [toml");
    }
