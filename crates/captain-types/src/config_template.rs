    #[test]
    fn render_is_valid_toml_and_roundtrips_to_kernel_config() {
        let s = render_default_toml().expect("render");
        let parsed: KernelConfig =
            toml::from_str(&s).expect("rendered template must be valid TOML for KernelConfig");
        // A couple of spot checks — we don't PartialEq the whole struct
        // because it contains non-Eq types (HashMap, PathBuf) that
        // survive the round-trip but don't implement Eq.
        assert_eq!(parsed.default_model.provider, "codex");
        assert_eq!(parsed.default_model.model, "gpt-5.5");
        assert_eq!(parsed.default_model.api_key_env, "");
        assert!(parsed.surfaces.is_active(crate::config::ActiveSurface::Cli));
        assert!(parsed.surfaces.is_active(crate::config::ActiveSurface::Web));
        assert!(parsed
            .surfaces
            .is_active(crate::config::ActiveSurface::Telegram));
        assert!(parsed.surfaces.is_frozen_name("hands"));
        assert_eq!(parsed.agent_loop.max_iterations, 90);
        assert!(parsed.learning.enabled);
        assert!(parsed.checkpoints.enabled);
        assert_eq!(parsed.checkpoints.model, "gpt-5.5");
        assert!(parsed.skills.enabled);
        assert_eq!(parsed.learning.reflection_model, "gpt-5.5");
        assert!(parsed.learning.fallback_models.is_empty());
        assert_eq!(parsed.skills.proposer_model, "gpt-5.5");
        assert!(parsed.skills.fallback_models.is_empty());
        assert_eq!(parsed.skills.pattern_threshold, 5);
        assert_eq!(parsed.tts.local_native.preferred_engine, "kokoro");
        assert_eq!(parsed.tts.local_native.fallback_engine, "piper");
        assert!(parsed.media.audio_model.is_none());
        assert!(parsed.voice_call.enabled);
        assert_eq!(parsed.voice_call.model, "gpt-realtime-2");
        assert_eq!(parsed.voice_call.auto_end_silence_secs, 90);
        assert_eq!(parsed.voice_call.auto_end_inactive_secs, 180);
    }

